#include "includes/libft.h"

void	ft_putnbr(int n)
{
	char a;

	if (n < 0)
	{
		write(1, "-", 1);
		if (n == -2147483648)
		{
			write(1, "2", 1);
			n = -147483648;
		}
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
	{
		a = n + '0';
		write(1, &a, 1);
	}
}
