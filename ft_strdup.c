#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *cpy;

	if (!(cpy = (char *)malloc(sizeof(*cpy) * (ft_strlen(s1) + 1))))
		return (NULL);
	ft_strcpy(cpy, s1);
	return (cpy);
}
